import os
import sys
import inspect
from qgis.PyQt.QtWidgets import QAction
from qgis.PyQt.QtGui import QIcon,QDesktopServices
from qgis.PyQt.QtCore import QSettings, QTranslator, QCoreApplication, QUrl
from qgis.core import QgsProcessingAlgorithm, QgsApplication
from .OpenWeather_provider import OpenWeatherProvider
import processing
from .urls import DOC_PLUGIN_URL
cmd_folder = os.path.split(inspect.getfile(inspect.currentframe()))[0]

if cmd_folder not in sys.path:
    sys.path.insert(0, cmd_folder)


class OpenWeatherPlugin(object):

    def __init__(self,iface):
        self.provider = None
        self.iface = iface

    def initProcessing(self):
        """Init Processing provider for QGIS >= 3.8."""
        self.provider = OpenWeatherProvider()
        QgsApplication.processingRegistry().addProvider(self.provider)

    def initGui(self):
        self.initProcessing()
        icon = os.path.join(os.path.join(cmd_folder, 'weather.png'))
        self.action = QAction(
          QIcon(icon),
          u"OpenWeather Current Data (SinglePoints)", self.iface.mainWindow())
        self.action.triggered.connect(self.weather)
        self.iface.addPluginToMenu(u"&OpenWeather", self.action)
        self.iface.addToolBarIcon(self.action)
        
        icons = os.path.join(os.path.join(cmd_folder, 'air.png'))
        self.actionAide= QAction(QIcon(icons),u"OpenWeather AirPollution Data (Historical)",self.iface.mainWindow())
        self.actionAide.triggered.connect(self.airhist)
        self.iface.addPluginToMenu(u"&OpenWeather", self.actionAide)
        self.iface.addToolBarIcon(self.actionAide)

        icons2 = os.path.join(os.path.join(cmd_folder, 'weather.png'))
        self.actionAide2= QAction(QIcon(icons2),u"OpenWeather Weather Data (5-days ago)",self.iface.mainWindow())
        self.actionAide2.triggered.connect(self.weatherhist)
        self.iface.addPluginToMenu(u"&OpenWeather", self.actionAide2)
        self.iface.addToolBarIcon(self.actionAide2)
        
        icons3 = os.path.join(os.path.join(cmd_folder, 'weather.png'))
        self.actionAide3= QAction(QIcon(icons3),u"OpenWeather Current Data (MultiPoints)",self.iface.mainWindow())
        self.actionAide3.triggered.connect(self.weathermulti)
        self.iface.addPluginToMenu(u"&OpenWeather", self.actionAide3)
        self.iface.addToolBarIcon(self.actionAide3)
        
        iconh = os.path.join(os.path.join(cmd_folder, 'help.png'))
        self.actionAideh= QAction(QIcon(iconh),u"help",self.iface.mainWindow())
        self.actionAideh.triggered.connect(self.showHelp)
        self.iface.addPluginToMenu(u"&OpenWeather", self.actionAideh)
        self.iface.addToolBarIcon(self.actionAideh)
        
    def unload(self):
        QgsApplication.processingRegistry().removeProvider(self.provider)
        self.iface.removePluginMenu(u"&OpenWeather", self.action)
        self.iface.removeToolBarIcon(self.action)
        
        self.iface.removePluginMenu(u"&OpenWeather", self.actionAide)
        self.iface.removeToolBarIcon(self.actionAide)
        
        self.iface.removePluginMenu(u"&OpenWeather", self.actionAide2)
        self.iface.removeToolBarIcon(self.actionAide2)
        
        self.iface.removePluginMenu(u"&OpenWeather", self.actionAideh)
        self.iface.removeToolBarIcon(self.actionAideh)
        
        self.iface.removePluginMenu(u"&OpenWeather", self.actionAide3)
        self.iface.removeToolBarIcon(self.actionAide3)
    def showHelp(self):  ### Help window
        QDesktopServices.openUrl(QUrl(DOC_PLUGIN_URL))
        
    def airhist(self):
        processing.execAlgorithmDialog("Open Weather Downloader:OpenWeather AirPollution Data (Historical)")
    def weatherhist(self):
        processing.execAlgorithmDialog("Open Weather Downloader:OpenWeather Weather Data (5-days ago)")
    def weather(self):
        processing.execAlgorithmDialog("Open Weather Downloader:OpenWeather Current Data (SinglePoints)")
    def weathermulti(self):
        processing.execAlgorithmDialog("Open Weather Downloader:OpenWeather Current Data (MultiPoints)")
        